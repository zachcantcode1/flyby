export const getAirlineName = (callsign: string | null | undefined): string | null => {
    if (!callsign || callsign.length < 3) return null;

    // ICAO codes are usually the first 3 letters of the callsign
    // Check first 3 characters
    const code = callsign.substring(0, 3).toUpperCase();
    return AIRLINES[code] || null;
};
